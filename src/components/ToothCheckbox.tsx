interface ToothSectionProps {
  id: number;
  onChange: any;
  paths: { d: string; fill: string }[];
  isChecked: boolean;
  showLabels: boolean;
  side: "left" | "right";
}

const ToothSection: React.FC<ToothSectionProps> = ({
  id,
  paths,
  onChange,
  isChecked,
  showLabels,
}) => {
  return (
    <g
      onMouseDown={(e) => {
        e.preventDefault();
        onChange(e, id);
      }}
      className="group cursor-pointer"
    >
      {paths.map((path, index) => (
        <path
          key={`${id}-path-${index}`}
          d={path.d}
          className={`transition-all duration-200`}
          style={{
            strokeWidth: 1,
            stroke: "#333",
            strokeOpacity: 0.5,
            fill: isChecked ? path.fill : "#ffffff05",
          }}
        />
      ))}
    </g>
  );
};

